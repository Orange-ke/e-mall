<script>
import { Tab, Tabs, Button, Cell, CellGroup, Radio, RadioGroup, Field, Toast } from 'vant'
import { store } from '../../../../store/store'
import axios from 'axios'
// import axios from 'axios'
export default {
  data () {
    return {
      loading: false,
      balance: 0,
      withDrawType: 2,
      minPrice: 1,
      active: 1,
      // bank type
      bankMoneyError: false,
      radio: 0,
      bankList: [],
      bankMoney: null,
      // alipay type
      alipayAccountError: false,
      alipayMoneyError: false,
      alipayAccount: '',
      alipayMoney: null,
      // wx type
      wxMoneyError: false,
      wxMonney: null,
      // 提交数据
      submitData: {}
    }
  },
  mounted: function () {
    axios
      .get('/api/Withdraw/getCommisionInfo')
      .then(res => {
        console.log(res)
        if (res.data.code === 200) {
          this.balance = res.data.data.balance
        } else {
          this.$toast.fail('请求错误')
        }
      })
      .catch(error => {
        console.log(error)
        this.$toast.fail('请求错误')
      })
  },
  methods: {
    changeType: function (index) {
      this.withDrawType = index + 1
      console.log(this.withDrawType)
    },
    validate: function () {
      switch (this.withDrawType) {
        case 1:
          if (this.bankType === 0) {
            Toast('请选择银行')
            return false
          }
          if (this.balance < 5) {
            Toast('提现金额必须大于5.00元')
            return false
          }
          break
        case 2:
          if (this.balance < 5) {
            Toast('提现金额必须大于5.00元')
            return false
          }
          break
        case 3:
          if (this.balance < 5) {
            Toast('提现金额必须大于5.00元')
            return false
          }
          if (!this.alipayAccount) {
            Toast('账户不能为空')
            return false
          }
          break
      }
      return true
    },
    submit: function () {
      switch (this.withDrawType) {
        case 1:
          this.submitData = {
            withDrawType: 'bank',
            bankType: this.radio,
            money: this.bankMoney ? this.bankMoney : 0
          }
          break
        case 2:
          this.submitData = {
            'extract_type': 'weixin',
            'alipay_code': '',
            'bank_address': '',
            'bank_code': '',
            'real_name': ''
          }
          break
        case 3:
          this.submitData = {
            'extract_type': 'alipay',
            'alipay_code': this.alipayAccount || '',
            'bank_address': '',
            'bank_code': '',
            'real_name': ''
          }
          break
      }
      if (!this.validate()) {
        return
      }
      this.loading = true
      console.log(this.submitData)
      axios
        .post('/api/Withdraw/add', this.submitData)
        .then(res => {
          console.log(res)
          if (res.data.code === 200) {
            this.loading = false
            this.$toast.success({
              message: '申请提现成功，请等待审核'
            })
            let payload = {...store.getters.userData}
            payload.balance = 0
            store.commit('updateUserData', payload)
          } else {
            this.loading = false
            this.$toast.fail({
              message: '申请失败，请重试'
            })
          }
        })
        .catch(error => {
          console.log(error)
          this.loading = false
          this.$toast.fail({
            message: '申请失败，请重试'
          })
        })
    }
  },
  components: {
    'van-tab': Tab,
    'van-tabs': Tabs,
    'van-button': Button,
    'van-cell': Cell,
    'van-cell-group': CellGroup,
    'van-radio': Radio,
    'van-radio-group': RadioGroup,
    'van-field': Field
  }
}
</script>

